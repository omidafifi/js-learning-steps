// 🟩 سؤال تمرینی — Step 05: عملگرهای ریاضی
// 🧠 دستورالعمل:
// کدی بنویس که کارهای زیر رو انجام بده:
// 	1.	یک متغیر به نام x تعریف کن و مقدار عددی 10 بهش بده.
// 	2.	یک متغیر به نام y تعریف کن و مقدار 3 بهش بده.
// 	3.	حاصل جمع x و y رو در یک متغیر جدید به نام sum ذخیره کن.
// 	4.	حاصل تفریق x - y رو چاپ کن.
// 	5.	مقدار sum رو با استفاده از typeof بررسی کن.
let x = 10;
let y = 3;
let sum = x + y;
console.log(x - y);
console.log(typeof sum);
// 🟦 سؤال تمرینی — Arithmetic Operators (تمرین ۲)
// 🧠 دستورالعمل:
// کدی بنویس که:
// 	1.	دو متغیر تعریف کن به نام‌های a = 15 و b = 4
// 	2.	حاصل ضرب a * b را در متغیر mul ذخیره کن
// 	3.	حاصل تقسیم a / b را در div ذخیره کن
// 	4.	باقیمانده تقسیم a % b را در mod ذخیره کن
// 	5.	همه‌ی این مقدارها (mul, div, mod) را با استفاده از console.log چاپ کن
// 	6.	با typeof بررسی کن که نوع div چی هست
let a = 15;
let b = 4;
let mul = a * b;
let div = a / b;
let mod = a % b;
console.log(mul, div, mod);
console.log(typeof div);

